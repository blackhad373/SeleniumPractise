package conditionalStatement;

public class NestedIfElseCondition {

	public static void main(String[] args) {
		int time=10;
		if(time<10)
		{
			System.out.println("Good morning");
		}
		else if(time<20)
		{
			System.out.println("Good afternoon");
		}
		else
		{
			System.out.println("Good noon");
		}
	}

}
